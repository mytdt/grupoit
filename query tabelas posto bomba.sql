-- Abertura e Fechamento - SL2010
SELECT  L2_EMISSAO, L2_TBOMBA, L2_LOCAL, L2_TBICO,MAX(L2_TENCFIM) FECHAMENTO,MIN(L2_TENCINI) ABERTURA, ROUND((MAX(L2_TENCFIM)-MIN(L2_TENCINI)),2) AS VENDAS_BICO,   ROUND(SUM(L2_QUANT),2) SOMA_QUANT 
FROM SL2010 
WHERE D_E_L_E_T_ = ' ' AND L2_FILIAL = '44' AND L2_EMISSAO BETWEEN '20150131' AND '20150201' AND L2_PRODUTO IN ('214673','231808','235244','214670')
AND L2_LOCAL IN ('01','02','03','04','05','06','07') AND L2_PDV <> ' '
GROUP BY  L2_EMISSAO,L2_PRODUTO,L2_LOCAL, L2_TBOMBA,  L2_TBICO
ORDER BY  L2_EMISSAO,L2_PRODUTO,L2_LOCAL, L2_TBOMBA,  L2_TBICO

-- Abertura e Fechamento - LEG010
SELECT  LEG_DATA, LEG_BOMBA, LEG_TANQUE, LEG_BICO,MAX(LEG_ENCFIM) FECHAMENTO,MIN(LEG_ENCINI) ABERTURA, ROUND((MAX(LEG_ENCFIM)-MIN(LEG_ENCINI)),2) AS VENDAS_BICO
FROM LEG010 
WHERE D_E_L_E_T_ = ' ' AND LEG_FILIAL = '44' AND LEG_DATA BETWEEN '20150131' AND '20150201' AND LEG_PROD IN ('214673','231808','235244','214670')
AND LEG_TANQUE IN ('01','02','03','04','05','06','07') 
GROUP BY  LEG_DATA,LEG_PROD,LEG_TANQUE, LEG_BOMBA,  LEG_BICO
ORDER BY  LEG_DATA,LEG_PROD,LEG_TANQUE, LEG_BOMBA,  LEG_BICO


